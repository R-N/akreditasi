<p>Untuk meningkatkan kompetensi tenaga kependidikan yang telah dilakukan sampai dengan saat ini, yakni:</p>
<p>
	<ol>
		<li>
			<p>Mengikuti kegiatan Short Course Pendidikan Manajemen dan Kepemimpinan.</p>
			<p>Kegiatan ini dilaksanakan di Universitas Canberra diselenggarakan pada tanggal 23 s/d 25 November 2015.</p>
		</li>
		<li>
			<p>Mengikuti Pelatihan Sistem Informasi Manajemen Administrasi Akademik (SIAKAD).</p>
			<p>Perangkat pada Sistem Informasi yang digunakan pada lingkungan UIN Sunan Ampel Surabaya untuk manajemen administrasi akademik dan kemahasiswaan adalah SIAKAD (Sistem Informasi Akademik). Pelatihan ini dimaksudkan untuk meningkatkan pengetahuan tenaga kependidikan prodi Arsitektur dalam pengelolaan manajemen SIAKAD. Pelatihan ini pada akhirnya bertujuan dalam rangka peningkatan pelayanan akademik terhadap dosen dan mahasiswa prodi.</p>
		</li>
		<li>
			<p>Pelatihan Instrumentasi bagi Laboran.</p>
			<p>Kegiatan dilaksanakan di laboratorium Integrasi UIN Sunan Ampel Surabaya. Pelatihan ini bertujuan untuk menambah keahlian (skill) laboran dalam menggunakan alat-alat laboratorium untuk kegiatan praktikum serta penelitan mahasiswa maupun dosen. </p>
		</li>
		<li>
			<p>Diklat Penyusunan Standard Operating Procedure (SOP)</p>
			<p>Kegiatan dilaksanakan pada tanggal 21 s/d 23 Desember 2017 di Kaliandra, Pasuruan. Peran SOP dalam layanan akademik akan meningkatkan pelaksanaan sesuai mutu dari waktu ke waktu. SOP sebagai alat untuk menyamakan persepsi antara berbagai pihak yang terlibat dalam suatu rangkaian kegiatan. SOP dapat berfungsi sebagai media pengendalian dan pemantauan mutu kinerja sehingga diklat ini dimaksudkan agar SOP lebih optimal. </p>
		</li>
		<li>
			<p>Diklat Sistem dan Prosedur Pertanggungjawaban Keuangan Badan Layanan Umum (BLU).</p>
			<p>Kegiatan ini diselenggarakan pada tanggal 21 s/d 23 Desember 2017 di Kaliandra, Pasuruan. Pengelolaan keuangan pada satuan kerja BLU berbeda dengan non BLU, sehingga diklat ini diperlukan. Materi diklat antara lain, penatausahaan kas BLU, pengelolaan keuangan, pertanggungjawaban pengelolaan uang dan pengendalian internal.</p>
		</li>
		<li>
			<p>Kursus Bahasa Inggris.</p>
			<p>Kegiatan ini merupakan kerjasama dengan Lembaga Bahasa yaitu, IALF (Indonesia Australia Language Foundation) yang beralamat di Jalan Sumatera 49 Surabaya. Kursus berlangsung selama dua bulan tepatnya pada tanggal 28 Juli s/d 1 Oktober 2015. Kegiatan ini bertujuan untuk meningkatkan kemampuan berahasa Inggris (Conversation) pada tenaga kependidikan dalam melayani mahasiswa asing atau kegiatan yang memerlukan kemampuan berbahasa Inggris.</p>
		</li>
		<li>
			<p>Studi Banding.</p>
			<p>Kegiatan studi banding dilakukan ke Universitas Diponegoro. Kegiatan ini diselenggarakan untuk melihat secara langsung pelayanan akademik pada Universitas negeri yang telah lama berdiri dan dianggap memiliki pengalaman lebih. Sehingga dapat menambah wawasan dan memperbaiki kekurangan yang ada.</p>
		</li>
		<li>
			<p>Pendidikan dan Pelatihan Membangun Karakter Pelayanan Publik (Capacity Building).</p>
			<p>Kegiatan ini diselenggarakan pada tanggal 09 s/d 11 November 2018. Pelatihan ini diperuntukkan bagi tenaga kependidikan bagi Pejabat Eselon IV bagi Aparatur Sipil Negara (ASN) di lingkungan UIN Sunan Ampel Surabaya melalui pola kemitraan dengan Badan Pendidikan dan Pelatihan Provinsi Jawa Timur.</p>
		</li>
	</ol>
</p>